#!/bin/bash

tput setaf 5
echo -e "\n*******************************************************************************************************************"
echo -e "Downloading the  KinD v0.11.1 Binary"
echo -e "*******************************************************************************************************************"
tput setaf 2
curl -Lo ./kind https://kind.sigs.k8s.io/dl/v0.11.1/kind-linux-amd64
chmod +x ./kind

tput setaf 5
echo -e "\n*******************************************************************************************************************"
echo -e "Moving the KinD Binary to /usr/bin"
echo -e "*******************************************************************************************************************"
tput setaf 2
sudo mv ./kind /usr/bin

tput setaf 3
echo -e "\n*******************************************************************************************************************"
echo -e "KinD installation complete"
echo -e "*******************************************************************************************************************"
tput setaf 2

